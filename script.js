let resul;

const plus = (x, y) => {
	resul = x + y;
	return resul;
};

const moin = (x, y) => {
	resul = x - y;
	return resul;
};

const foix = (x, y) => {
	resul = x * y;
	return resul;
};

const divi = (x, y) => {
	resul = x / y;
	return resul;
};

const puis = (x, y) => {
	resul = x ** y;
	return resul;
};

const reset = () => {
	resul = 0;
};
